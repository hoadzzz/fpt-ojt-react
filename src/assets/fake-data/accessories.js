const getAllAccessories = () => accessories

const getAccessories = (count) => {
    const max = accessories.length - count
    const min = 0
    const start = Math.floor(Math.random() * (max - min) + min)
    return accessories.slice(start, start + count)
}
const getAccessoryBySlug = (slug) => accessories.find(e => e.slug === slug)

const getCartItemsInfo = (cartItems) => {
    let res = []
    if (cartItems.length > 0) {
        cartItems.forEach(e => {
            let product = getAccessoryBySlug(e.slug)
            res.push({
                ...e,
                product: product
            })
        })
    }
    return res.sort((a, b) => a.id > b.id ? 1 : (a.id < b.id ? -1 : 0))
}

const accessoriesData = {
    getAllAccessories,
    getAccessories,
    getAccessoryBySlug,
    getCartItemsInfo
}

export default accessoriesData